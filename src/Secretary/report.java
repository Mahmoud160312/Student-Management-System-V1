    private void jTextField12FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField12FocusLost

        //********* to show the num of exam after enter the name *********
        try {
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms", "root", "root");
            PreparedStatement stmt = con.prepareStatement("select *from student");

            ResultSet set = stmt.executeQuery();

            String check_name = "", name = "", num = "", level_name = "";
            check_name = jTextField12.getText();

            while (set.next()) {
                name = set.getString("name");

                if (name.equals(check_name)) {

                    num = set.getString("student_id");
                    level_name = set.getString("student_level");
                    break;
                }

            }

            if (name.equals(check_name)) {

                jTextField9.setText(num);
                jTextField14.setText(level_name);
                jTextField9.setVisible(true);

            } else if (!check_name.equals(name) && !check_name.equals("")) {

                JOptionPane.showMessageDialog(this, "يرجى التاكد من اسم الطالب", "تحذير", JOptionPane.INFORMATION_MESSAGE);
                jTextField12.setText("");
                jTextField9.setText("");
            }

        } catch (SQLException ex) {
            System.out.println("Error" + ex.getErrorCode());
        }
    }//GEN-LAST:event_jTextField12FocusLost

